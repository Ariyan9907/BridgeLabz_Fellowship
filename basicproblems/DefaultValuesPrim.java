package com.src.basicproblems;

public class DefaultValuesPrim {
    byte by;
    int in;
    short sh;
    long ln;
    float fl;
    double dl;
    char ch;
    boolean bl;

    public static void main(String args[]){
        DefaultValuesPrim d=new DefaultValuesPrim();
        System.out.println("Byte:"+d.by);
        System.out.println("Int:"+d.in);
        System.out.println("Short:"+d.sh);
        System.out.println("long:"+d.ln);
        System.out.println("Float:"+d.fl);
        System.out.println("Double:"+d.dl);
        System.out.println("Char:"+d.ch);
        System.out.println("Boolean:"+d.bl);

    }
}
